package com.sunbeam;

public class Arithmetic {
	public Integer sum(Integer a, Integer b) {
		return a + b;
	}
	public Double sum(Double a, Double b) {
		return a + b;
	}
	public Integer subtract(Integer a, Integer b) {
		return a - b;
	}
	private Integer multiply(Integer a, Integer b) {
		return a * b;
	}
}
